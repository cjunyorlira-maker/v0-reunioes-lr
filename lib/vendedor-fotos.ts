// Funcao para obter genero do vendedor
export function getVendedorGenero(nome: string): "M" | "F" {
  if (!nome) return "M"
  const normalizado = normalizeVendedorNome(nome.trim())
  return VENDEDOR_GENERO[normalizado] || "M" // default masculino se nao encontrar
}

// Mapa de nomes que devem ser unificados (nome curto -> nome completo)
const VENDEDOR_ALIAS: Record<string, string> = {
  "Alexia": "Alexia Cunha",
  "Aléxia": "Alexia Cunha",
  "Lidiane": "Lidiane Fonseca",
  "Rafaella": "Rafaella Antunes",
}

// Função para normalizar nome de vendedor (unifica variações)
export function normalizeVendedorNome(nome: string): string {
  if (!nome) return nome
  const trimmed = nome.trim()
  return VENDEDOR_ALIAS[trimmed] || trimmed
}

// Função auxiliar — busca a foto pelo nome, com fallback por primeiro nome
export function getFotoVendedor(nome: string): string | undefined {
  if (!nome) return undefined

  // Tentativa exata
  if (VENDEDOR_FOTOS[nome]) return VENDEDOR_FOTOS[nome]

  // Tentativa case-insensitive
  const nomeNorm = nome.trim().toLowerCase()
  const found = Object.entries(VENDEDOR_FOTOS).find(
    ([key]) => key.toLowerCase() === nomeNorm
  )
  if (found) return found[1]

  // Tentativa pelo primeiro nome + segundo nome (ignora sobrenomes extras)
  const partes = nomeNorm.split(" ")
  if (partes.length >= 2) {
    const prefixo = partes.slice(0, 2).join(" ")
    const foundPartial = Object.entries(VENDEDOR_FOTOS).find(
      ([key]) => key.toLowerCase().startsWith(prefixo)
    )
    if (foundPartial) return foundPartial[1]
  }

  return undefined
}
